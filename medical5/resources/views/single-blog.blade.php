                            <p> <b>Operation technique</b> <br>

All patients were under general anesthesia with double-lumen intubation and CO2 insufflation of 5–10 mmHg if CTS was performed [11, 12]. The patient lied on the semilateral decubitus position. Thoracoscopic surgery was performed with CTS (skin incision < 1 cm – only used with trocars 5 mm, 10 mm or 12 mm) or VATS approach. In CTS, the surgeon stood behind the patient and used Karl-Storz endoscopic instruments to set up trocars as type IV of Sasaki [13]. The first trocar for the camera was located in 6th or 7th ICS on the posterior axillary line. Other trocars (the 2nd or 3rd trocar) were placed depending on the location of tumor that usually aligned according to the principle of “baseball diamond concept” [14] (The second one is into the 3th or 4th ICS on the same line as the first trocar. The target trocar is usually on the 3th mid-clavicular line or the 7th ICS on the anterior axillary line). In VATS, we kept placing the first trocar in the 6th or 7th ICS on the posterior axillary line as in CTS, but the 2 other trocars in CTS were replaced in VATS by a small incision which was about 2 to 4 cm in length (without spreading the ribs) in 5th ICS between linea axillaris anterior and linea axillaris posterior. This incision site was aimed to avoid scarring in anterior area and to hide the surgical scar by arm movements or bras (if patients were female), but still guaranteed it was the appropriate site to approach teratomas. We used VATS if the tumor was large (> 6 cm in diameter), had thick pleural adhesions or invasively encroached into the surrounding organs that make it difficult to be completely dissected by thoracoscopic surgery. Dissection and complete removal of tumors were performed with an electrocautery, harmonic scalpel, ligasure or silver clip (for hemostasis). In some cases, if a teratoma mainly contained fluid, we would extract its contents before dissecting and removing them. If the tumor was small, it was taken out of the chest by putting into a plastic bag, through a trocar hole or through a 2 - 4 cm skin incision extended from a trocar hole. Otherwise, alarge tumor would be divided into smaller piecies and removed through a small hole on the chest wall. The specimens were sent to pathological department. Hemostasis was managed before we finishish the operation. Usually, a pleural drainage of 32F or 18F silicone was used under the camera’s guidance to expand the lungs before removal of the trocars and closure of the surgical wounds.</p>
