                            <a href="#" class="headline mb-0">Thoracoscopic surgery approach to mediastinal mature teratomas: a single-center experience </a>
                            <div class="post-meta">
                                <a href="#" class="post-author-name">by Nezar Omran</a> | <a href="#" class="post-catagory">Cardiothoracic Surgery</a>
                            </div>
                            <p>Mediastinal mature teratomas (MMT), accounting for 5 to 10% of all mediastinal tumors [1], are usually located in the anterior mediastinum,. They can be cured with complete surgical resection with favorable prognosis [2]. Open thoracotomy is still preferred in cases with large or malignant tumors [3]. Sato et al. compared the efficacy and safety of mediastinal tumor resection in children used open thoracotomy (OT) versus VATS and suggested that VATS was the less invasive method because of its fewer blood transfusion requirements and shorter thoracic drainage duration and hospital stay [4]. VATS was widely used for the resection of mediastinal masses [5, 6], with many successful VATS procedures to remove benign mediastinal tumors have been reported [6]. With the development of science and technology in operative techniques, thoracoscopic surgery such as closed thoracoscopic surgery (CTS) or video-assisted thoracoscopic surgery (VATS) has become more popular [7, 8] and currently has been accepted and widely deployed by many thoracic surgeons in the world. However, the roles of tumor sizes, grades of adhesion and surgical experience in thoracoscopy decision were remained unknown [9, 10]. The aim of this study was to clarify these aspects in thoracoscopic surgery for mediastinal mature teratomas that has been applied at the Department of Cardiovascular & Thoracic Surgery – Viet Duc University Hospital.</p>
